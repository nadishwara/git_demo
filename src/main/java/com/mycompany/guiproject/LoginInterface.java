    private void LoginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginbtnActionPerformed
        
        
        String enterUserId = UsernameTextField.getText().trim();
        String enterPassword = PasswordTextField1.getText().trim();
        boolean loginSuccessful = false;

        try (BufferedReader reader = new BufferedReader(new FileReader("G:\\GUIproject\\Cashierregistration.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] Cashierregistratio = line.split(",");
                if (Cashierregistratio.length < 3) {
                    continue; // Skip if line is malformed
                }
                String storedUserId = Cashierregistratio[0].trim();
                String storedPassword = Cashierregistratio[1].trim();

                System.out.println("Checking: " + storedUserId + " | " + storedPassword);
                System.out.println("Entered: " + enterUserId + " | " + enterPassword);

                if (enterUserId.equals(storedUserId) && enterPassword.equals(storedPassword)) {
                    loginSuccessful = true;
                    break;
                }
            }
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(this, "File not found: " + e.getMessage());
            return; // Early return if file is not found
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading file: " + e.getMessage());
            return; // Early return if there's an IO error
        }

        if (loginSuccessful) {
            JOptionPane.showMessageDialog(this, "Login Success!");
            // Proceed to the next screen or main application
        } else {
            JOptionPane.showMessageDialog(this, "Invalid username or password, please try again.");
        }
        
        if (loginSuccessful) {
        JOptionPane.showMessageDialog(this, "Login Success!");
        // Proceed to show the Dashboard
        ViewSupplierManager dashview = new ViewSupplierManager();
        dashview.setVisible(true);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "Invalid username or password, please try again.");
    }


    }//GEN-LAST:event_LoginbtnActionPerformed

